void BitmapFont::DrawCharacter(
    Framebuffer& framebuffer,
    int x,
    int y,
    char character,
    uint32_t color)
{
    const char* glyph = GlyphFor(character);

    for (int row = 0; row < 7; ++row)
    {
        for (int column = 0; column < 5; ++column)
        {
            const int index = row * 5 + column;

            if (glyph[index] == '1')
            {
                framebuffer.SetPixel(x + column, y + row, color);
            }
        }
    }
}

void BitmapFont::DrawString(
    Framebuffer& framebuffer,
    int x,
    int y,
    const char* text,
    uint32_t color)
{
    int cursorX = x;

    for (std::size_t i = 0; text[i] != '\0'; ++i)
    {
        DrawCharacter(framebuffer, cursorX, y, text[i], color);
        cursorX += 7;
    }
}

